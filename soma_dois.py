# Definindo a função ...
def somar_numeros(a, b):
    # retorna a soma de dois números
    return a + b

# Testar a função com alguns exemplos
print('Os resultados são: ')
print('A soma de 3 + 5 =', somar_numeros(3, 5)) # Deve retornar 8
print('A soma de (-2) + 7 =', somar_numeros(-2, 7)) # Deve retornar 5
print('A soma de (-10) + 10 =', somar_numeros(-10, 10)) # Deve retornar 0

print()

print('Teste da função realizada com sucesso!')

