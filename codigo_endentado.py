# Exemplo de trecho de código em bloco endentado corretamente!
def spam():
    eggs = 12
    return eggs

print(spam())

print()

# Pode ser perceber que o Python usa a endentação como delimitação de bloco.

print('=' * 10)
print()

# Se o bloco tem apenas um comando, pode-se escrever tudo em uma linha:

n = -1
